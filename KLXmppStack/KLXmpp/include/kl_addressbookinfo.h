#ifndef KL_ADDRESSBOOKINFO_H__
#define KL_ADDRESSBOOKINFO_H__

#include <string>

namespace kl
{
	/**
	 * @brief 通讯录信息实体。
	 *
	 * @author JI Yixuan
	 */
	struct AddressBookInfo
	{
		// 默认构造函数，设置默认值
		AddressBookInfo()
		{
			id = "";
			name = "";
			isAvailable = false;
		}

		/**
		 * ID。
		 */
		std::string id;

		/**
		 * 名称。
		 */
		std::string name;
				
		/**
		 * 是否可用。
		 */
		bool isAvailable;
	};
}

#endif // KL_ADDRESSBOOKINFO_H__